def detect_profession_category(text):
    """
    텍스트에서 18개 직업 카테고리 키워드 매칭.
    Returns: (category_key, score) 리스트 (점수순 정렬)
    """
    if not text:
        return []

    text_lower = text.lower()
    scores = {}

    for category, keywords in PROFESSION_KEYWORDS.items():
        score = 0
        for kw in keywords:
            if kw in text_lower or kw in text:
                score += 1
        if score > 0:
            scores[category] = score

    ranked = sorted(scores.items(), key=lambda x: x[1], reverse=True)
    return ranked


def detect_protagonist_type(text):
    """
    텍스트에서 주인공 유형 감지.
    Returns: 유형 키 (기본값 '정통_주인공')
    """
    if not text:
        return "정통_주인공"

    scores = {}
    for ptype, keywords in PROTAGONIST_TYPE_KEYWORDS.items():
        if not keywords:
            continue
        score = 0
        for kw in keywords:
            if kw in text:
                score += 1
        if score > 0:
            scores[ptype] = score

    if not scores:
        return "정통_주인공"

    return max(scores, key=scores.get)


