    base = SYSTEM_PROMPT

    if narrative_tone:
        tone_block = get_narrative_tone_block(narrative_tone)
        if tone_block:
            base += f"\n\n## 이 작품의 톤\n{tone_block}"

    if for_episode_write:
        base += "\n\n" + FICTION_FRAME_DECLARATION
        base += "\n\n" + ABSOLUTE_LIMITS

    return base


