def get_motif_block(primary_motif="", secondary_motif=""):
    """주요 모티프 블록 생성."""
    blocks = []
    for label, motif_key in [("1차 모티프", primary_motif), ("2차 모티프", secondary_motif)]:
        if motif_key and motif_key in NARRATIVE_MOTIFS:
            m = NARRATIVE_MOTIFS[motif_key]
            blocks.append(f"**{label} · {motif_key}**: {m['desc']}\n  활용: {m['usage']}")
    if blocks:
        return "## 서사 모티프\n" + "\n\n".join(blocks)
    return ""


