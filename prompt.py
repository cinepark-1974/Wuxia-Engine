def get_formula_block(formula_keys):
    """
    선택된 공식(들)의 상세 블록 생성.
    formula_keys: 단일 키 문자열 혹은 리스트
    """
    if isinstance(formula_keys, str):
        formula_keys = [formula_keys]

    blocks = []
    for key in formula_keys:
        formula = WUXIA_FORMULAS.get(key)
        if not formula:
            continue

        beat_text = "\n".join([
            f"  - EP {b['ep']} [{b['name']}] {b['desc']}"
            for b in formula["beat_structure"]
        ])

        block = f"""
## 공식: {formula['label']}
설명: {formula['description']}
레퍼런스: {', '.join(formula['references'])}
주 독자: {formula['target_age']}
포화도: {formula['saturation']}/10

비트 구조 (50화):
{beat_text}
""".strip()
        blocks.append(block)

    return "\n\n".join(blocks)


def match_formulas(idea_text, top_n=2):
    """
    아이디어 텍스트에서 상위 N개 공식 매칭.
    Returns: [(formula_key, score, label), ...]
    """
    if not idea_text:
        return []

    scores = {}
    for formula_key, keywords in FORMULA_KEYWORDS_WEIGHT.items():
        score = 0.0
        for keyword, weight in keywords.items():
            if keyword in idea_text:
                score += weight
        scores[formula_key] = score

    ranked = sorted(scores.items(), key=lambda x: x[1], reverse=True)
    result = []
    for formula_key, score in ranked[:top_n]:
        if score > 0:
            label = WUXIA_FORMULAS[formula_key]["label"]
            result.append((formula_key, score, label))
    return result


