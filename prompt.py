def detect_genre_key(genre_input):
    """입력 텍스트에서 장르 키 감지."""
    if not genre_input:
        return "무협"
    g = genre_input.lower()
    if any(k in g for k in ["회귀", "전생", "빙의", "시스템", "이세계", "환생"]):
        return "판타지_무협"
    if any(k in g for k in ["여성향", "여주", "로맨스"]):
        return "여성향_무협"
    return "무협"


